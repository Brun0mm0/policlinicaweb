                  <p><strong>¿QUÉ ES LA VACUNA ANTIGRIPAL?</strong></p>
                </div>
                <p>La  vacuna antigripal es una vacuna polivalente que contiene tres cepas de virus  gripal fraccionado; la frecuente variación de las cepas hace que sea necesario  adecuar la composición para que represente a los virus que probablemente vayan  a circular en la estación invernal. Es importante su aplicación ya que el virus  de la gripe cambia año tras año. Mejora el sistema inmune para defendernos de  la gripe, nos ayuda a tener un poco más de respaldo para hacerle frente al  COVID y reduce el riesgo de co-infecciones.</p>
                <div>
                  <p><strong>¿LA VACUNA ANTIGRIPAL PROTEGE DEL COVID-19?</strong></p>
                </div>
                <p>La  vacuna no protege contra el COVID-19, pero las personas mayores de 65 años y  los grupos de riesgo deben aplicársela para disminuir las complicaciones  vinculadas al virus de la inﬂuenza que predomina en la época invernal, más aún  en la actual situación epidemiológica.</p>
              <div>
                  <p><strong>¿CUÁL ES LA POBLACIÓN DE RIESGO QUE TIENE  LA OBLIGACIÓN DE VACUNARSE?</strong></p>
                </div>
                <ul>
                  <li>Personal de la  salud</li>
                  <li>Las personas mayores  de 65 años</li>
                  <li>Embarazadas  (cualquier trimestre) y puérperas (con niños menores de 6 meses de vida)</li>
                  <li>Niños de 6 meses a 2  años de vida, independientemente de los factores de riesgo</li>
                  <li>Mayores de 2 años de  edad hasta 64 años inclusive que pertenezcan a alguno de los siguientes grupos  en riesgo o tengan alguna de las siguientes condiciones:                </li>
                </ul>
                <ul>
                  <li><strong>Enfermedad respiratoria crónica:</strong> hernia diafragmática, EPOC, enﬁsema congénito, displasia  broncopulmonar, traqueostomizados crónicos, bronquiectasias, ﬁbrosis quística,  asma moderada y grave.</li>
                  <li><strong>Enfermedades cardíacas:</strong> cardiopatía congénita, insuﬁciencia cardíaca, enfermedad coronaria,  reemplazo valvular, valvulopatía.</li>
                  <li><strong>Inmunodeﬁciencias congénitas o adquiridas (no  oncohematológicas)</strong>: VIH, tratamiento  inmunosupresor o corticoterapia a altas dosis (≥ 2 mg/kg/día o 20 mg/día de  metilprednisona o su equivalente por más de 14 días), inmunodeﬁciencias  primarias, asplenia funcional o anatómica, desnutrición grave.</li>
                  <li><strong>Pacientes oncohematológicos y trasplantados:</strong> tumor de órgano sólido en tratamiento, enfermedad  oncohematológica hasta 6 meses después de su remisión completa, trasplantados  de órganos sólidos o tejido hematopoyético.</li>
                  <li><strong>Otros: </strong>obesos con  índice de masa corporal superior a 40, diabéticos, insuﬁciencia renal crónica  en diálisis o con expectativas de ingresar a diálisis en los siguientes 6  meses, retraso madurativo grave en menores de 18 años, síndromes genéticos,  enfermedades neuromusculares con compromiso respiratorio, malformaciones  congénitas graves, tratamiento crónico con ácido acetilsalicílico en menores de  18 años.</li>
                  <li><strong>Convivientes de enfermos oncohematológicos y de lactantes  prematuros</strong> con peso al nacimiento menor  a 1.500 gramos.</li>
              </ul>
